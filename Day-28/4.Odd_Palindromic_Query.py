def process_queries(N, Q, Arr, Query):
    """
    Write your logic here.
    Parameters:
        N (int): Size of the array
        Q (int): Number of queries
        Arr (list): List of integers representing the array
        Query (list): List of integers representing the queries
    Returns:
        list: List of integers representing the answer to each query
    """
    Answer = []
    for i in Query:
        # print(i)
        p=1
        # v=Arr[i]
        count=1
        if i==1:
            Answer.append(0)
        elif i==len(Arr):
            Answer.append(1)
        else:
            # print(i)
            while (i+p)<=(len(Arr)):
                # print("Inside While")
                if ((i-p-1)>=0)==((len(Arr))>=(i+p)):
                    # print("i+P: ",i+p)
                    v=Arr[i+p-1]
                    # print("While first if: ",i)
                    if Arr[i-p-1]==v:
                        # print("While Second if: ",i)
                        count+=2
                    else:
                        break
                p+=1
            if count%2==1:
                Answer.append(count)
            else:
                Answer.append(count-1)
    
    return Answer


